package br.com.hospital;

public class Enderecos {
	String enderecoPaciente;
	String enderecoMedico;
	String enderecoClinica;
	
	public String getEnderecoPaciente() {
		return enderecoPaciente;
	}
	public void setEnderecoPaciente(String enderecoPaciente) {
		this.enderecoPaciente = enderecoPaciente;
	}
	public String getEnderecoMedico() {
		return enderecoMedico;
	}
	public void setEnderecoMedico(String enderecoMedico) {
		this.enderecoMedico = enderecoMedico;
	}
	public String getEnderecoClinica() {
		return enderecoClinica;
	}
	public void setEnderecoClinica(String enderecoClinica) {
		this.enderecoClinica = enderecoClinica;
	}
}
